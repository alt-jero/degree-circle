/**
 * Return whether two angles are equal
 * @param {number} a a value in degrees
 * @param {number} b a value in degrees
 * @returns {boolean} whether the two inputs are === equal
 * @description
 * an angle is equal to another angle if:
 * - the angles have the same degree value
 */
const equal = (a, b) => a === b

module.exports = equal